class a 
{
	public static void main(String[] args) 
	{
		int i=10;
		System.out.println(i); //i=10;
		System.out.println(i++);//i=10;
		System.out.println(i);//i=11;
		System.out.println(++i);//i=12;
		System.out.println(i);//i=12;
	}
}
